MODULE PROCEDURE SAVE_DIMPHYEX_T
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_DIMPHYEX_T
END INTERFACE



     




CONTAINS

SUBROUTINE SAVE_DIMPHYEX_T (SELF, KLUN)

IMPLICIT NONE
TYPE (DIMPHYEX_T), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

WRITE (0, *) __FILE__, ':', __LINE__
WRITE (KLUN) SELF%NIT
WRITE (0, *) __FILE__, ':', __LINE__
WRITE (KLUN) SELF%NIB
WRITE (KLUN) SELF%NIE
WRITE (KLUN) SELF%NJT
WRITE (KLUN) SELF%NJB
WRITE (KLUN) SELF%NJE
WRITE (KLUN) SELF%NKL
WRITE (KLUN) SELF%NKT
WRITE (0, *) __FILE__, ':', __LINE__
WRITE (KLUN) SELF%NKLES
WRITE (KLUN) SELF%NKA
WRITE (KLUN) SELF%NKU
WRITE (KLUN) SELF%NKB
WRITE (KLUN) SELF%NKE
WRITE (KLUN) SELF%NKTB
WRITE (KLUN) SELF%NKTE
WRITE (KLUN) SELF%NIBC
WRITE (0, *) __FILE__, ':', __LINE__
WRITE (KLUN) SELF%NJBC
WRITE (KLUN) SELF%NIEC
WRITE (KLUN) SELF%NJEC
WRITE (KLUN) SELF%NIJT
WRITE (KLUN) SELF%NIJB
WRITE (KLUN) SELF%NIJE
WRITE (KLUN) SELF%NLESMASK
WRITE (KLUN) SELF%NLES_TIMES
WRITE (0, *) __FILE__, ':', __LINE__
END SUBROUTINE

