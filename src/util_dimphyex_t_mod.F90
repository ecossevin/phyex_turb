READ (KLUN) SELF%NKTB
READ (KLUN) SELF%NKTE
READ (KLUN) SELF%NIBC
READ (KLUN) SELF%NJBC
READ (KLUN) SELF%NIEC
READ (KLUN) SELF%NJEC
READ (KLUN) SELF%NIJT
READ (KLUN) SELF%NIJB
