MODULE UTIL_TFILEDATA_MOD

USE MODD_IO, ONLY : TFILEDATA

INTERFACE SAVE
MODULE PROCEDURE SAVE_TFILEDATA
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TFILEDATA
END INTERFACE



     




CONTAINS

SUBROUTINE SAVE_TFILEDATA (SELF, KLUN)

IMPLICIT NONE
TYPE (TFILEDATA), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

WRITE (KLUN) SELF%LOPENED
WRITE (KLUN) SELF%NLU
END SUBROUTINE

SUBROUTINE LOAD_TFILEDATA (SELF, KLUN)

IMPLICIT NONE
TYPE (TFILEDATA), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

READ (KLUN) SELF%LOPENED
READ (KLUN) SELF%NLU
END SUBROUTINE






     







END MODULE
